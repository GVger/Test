package cn.vger.schedule.model;

public class UserInfo {
	private String user;
	private String psw;
	
	public UserInfo(){}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPsw() {
		return psw;
	}

	public void setPsw(String psw) {
		this.psw = psw;
	}
}
